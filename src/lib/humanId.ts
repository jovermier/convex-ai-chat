/**
 * Generates a human-readable ID in the format: "word-word-00"
 * For example: "happy-panda-47", "swift-eagle-12"
 */
export function generateHumanId(): string {
  const adjective = ADJECTIVES[Math.floor(Math.random() * ADJECTIVES.length)]
  const noun = NOUNS[Math.floor(Math.random() * NOUNS.length)]
  const number = Math.floor(Math.random() * 100)
  return `${adjective}-${noun}-${number.toString().padStart(2, "0")}`
}

/**
 * Validates if a string matches the human ID format
 */
export function isValidHumanId(id: string): boolean {
  return HUMAN_ID_PATTERN.test(id)
}
